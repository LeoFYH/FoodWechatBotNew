create table if not exists tenants (
    id bigint generated by default as identity primary key,
    code text not null unique,
    name text not null,
    status text not null default 'active',
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint tenants_status_check check (status in ('active', 'disabled'))
);

create table if not exists users (
    id bigint generated by default as identity primary key,
    tenant_id bigint not null references tenants(id),
    display_name text not null default '',
    phone text,
    role text not null default 'member',
    status text not null default 'active',
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint users_role_check check (role in ('owner', 'admin', 'member', 'operator')),
    constraint users_status_check check (status in ('active', 'disabled'))
);

create table if not exists user_identities (
    id bigint generated by default as identity primary key,
    tenant_id bigint not null references tenants(id),
    user_id bigint references users(id),
    provider text not null,
    external_id text not null,
    open_kfid text,
    chat_id text,
    raw_profile jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    unique (tenant_id, provider, external_id)
);

create table if not exists api_clients (
    id bigint generated by default as identity primary key,
    tenant_id bigint not null references tenants(id),
    name text not null,
    token_hash text not null,
    scopes text[] not null default '{}'::text[],
    status text not null default 'active',
    created_at timestamptz not null default now(),
    rotated_at timestamptz,
    constraint api_clients_status_check check (status in ('active', 'disabled'))
);

create table if not exists stores (
    id bigint generated by default as identity primary key,
    tenant_id bigint not null references tenants(id),
    code text,
    name text not null,
    region text,
    address text,
    status text not null default 'active',
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique (tenant_id, name),
    constraint stores_status_check check (status in ('active', 'disabled'))
);

create table if not exists store_aliases (
    id bigint generated by default as identity primary key,
    tenant_id bigint not null references tenants(id),
    store_id bigint not null references stores(id) on delete cascade,
    alias text not null,
    source text not null default 'manual',
    created_at timestamptz not null default now(),
