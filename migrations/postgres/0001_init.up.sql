    order_date date not null,
    deliver_date date,
    change_type text,
    parent_order_id bigint references orders(id),
    raw_text text,
    raw_ref text,
    raw_payload jsonb not null default '{}'::jsonb,
    legacy_source text,
    legacy_id bigint,
